print(10 + 5) #addition operator
print(10 * 5) #multiplication operator
print(9 / 5) #division operator (provides quotient)
print(9 // 5) #floor division (provides quotient without decimal value)
print(10 % 5) #modulus operator(provides remainder)
print(10 - 5) #subtraction operator
print(5 ** 3) #exponential operator (provides power of a number)